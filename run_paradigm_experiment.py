"""Paradigm subset experiment: morphologically rich 200-type corpus.

Corpus: data/words_paradigm.txt — selected to include z/s/Iz paradigm pairs
like sak/saks, h@nd/h@ndz, kIs/kIsIz.  Generated by build_paradigm_subset.py.

Goal: verify that the model can learn z→s and z→z alternations from
morphologically diverse data, even with the current n=1 PYP formula.
"""

import sys
from pathlib import Path

# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------
N_SWEEPS       = 300
BURN_IN        = 100
SEED           = 42
IDENTITY       = False
PYP_DISCOUNT   = 0.5
PYP_CONC       = 1.0
PRIOR_SCALE    = 2.25
EPENTH_PRIOR   = 1.0
DELET_PRIOR    = 1.0
PRIOR_SIGMA    = 1.0

KLATTBET_IPA = {
    "D": "ð", "T": "θ", "S": "ʃ", "Z": "ʒ", "G": "ŋ",
    "C": "tʃ", "J": "dʒ", "I": "ɪ", "E": "ɛ", "U": "ʊ",
    "O": "ɔɪ", "W": "aʊ", "Y": "aɪ", "R": "ɝ", "@": "æ", "^": "ʌ",
}

# ---------------------------------------------------------------------------
# Load data
# ---------------------------------------------------------------------------
surface_forms = Path("data/words_paradigm.txt").read_text(encoding="utf-8").splitlines()
surface_forms = [w for w in surface_forms if w.strip()]

from collections import Counter
type_counts = Counter(surface_forms)

print(f"Training on {len(surface_forms)} tokens ({len(type_counts)} types)")
print(f"Config: N_SWEEPS={N_SWEEPS}, BURN_IN={BURN_IN}, PRIOR_SCALE={PRIOR_SCALE}, "
      f"IDENTITY={IDENTITY}, PYP_DISCOUNT={PYP_DISCOUNT}")
print()

# Show key paradigm types
for stem, sfx_pairs in [('sak', ['saks']), ('h@nd', ['h@ndz']),
                         ('kIs', ['kIsIz']), ('bUk', ['bUks']),
                         ('TIG', ['TIGz']), ('lEg', ['lEgz']),
                         ('mami', ['mamiz']), ('bebi', ['bebiz'])]:
    variants = [(v, type_counts.get(v, 0)) for v in [stem] + sfx_pairs]
    print("  " + ", ".join(f"{v}({c})" for v, c in variants if c > 0))
print()

# ---------------------------------------------------------------------------
# Fit model
# ---------------------------------------------------------------------------
from agme import Model
import time

t0 = time.perf_counter()
model = Model(
    morpheme_classes=["stem", "suffix"],
    pyp_discount=PYP_DISCOUNT,
    pyp_concentration=PYP_CONC,
    identity_phonology=IDENTITY,
    ipa_map=KLATTBET_IPA,
    prior_scale=PRIOR_SCALE,
    epenthesis_prior=EPENTH_PRIOR,
    deletion_prior=DELET_PRIOR,
    prior_sigma=PRIOR_SIGMA,
)
model.fit(
    surface_forms,
    n_sweeps=N_SWEEPS,
    burn_in=BURN_IN,
    maxent_update_every=10,
    print_every=50,
    seed=SEED,
)
elapsed = time.perf_counter() - t0
print(f"\nTraining finished in {elapsed:.1f}s")

# ---------------------------------------------------------------------------
# Metric 1: UR diversity
# ---------------------------------------------------------------------------
print("\n" + "="*65)
print("METRIC 1: UR posterior diversity")
print("="*65)

ur_post = model._training_state.ur_posterior
if ur_post:
    top_urs = sorted(ur_post.items(), key=lambda x: -x[1])[:20]
    print(f"Total unique URs in posterior: {len(ur_post)}")
    print(f"\nTop-20 URs by posterior probability:")
    for ur, prob in top_urs:
        print(f"  {ur:20s}  {prob:.4f}")
    top1_prob = top_urs[0][1]
    print(f"\nTop-1 UR mass: {top1_prob:.4f}  "
          f"({'COLLAPSED' if top1_prob > 0.5 else 'OK'})")
else:
    print("  (no posterior accumulated)")

# ---------------------------------------------------------------------------
# Metric 2: Phonological weight deviation from P-map priors
# ---------------------------------------------------------------------------
print("\n" + "="*65)
print("METRIC 2: Phonological weight deviation from P-map priors")
print("="*65)

import numpy as np
weights  = model.phonology.weights
prior_mu = model.phonology._prior_mu
deviations = weights - prior_mu
n_constraints = len(weights)

print(f"Total constraints: {n_constraints}")
print(f"Mean |deviation|:  {np.mean(np.abs(deviations)):.4f}")
print(f"Max  |deviation|:  {np.max(np.abs(deviations)):.4f}")
print(f"Constraints with |dev| > 0.001: {np.sum(np.abs(deviations) > 0.001)} / {n_constraints}")
print(f"Constraints with |dev| > 0.01:  {np.sum(np.abs(deviations) > 0.01)} / {n_constraints}")
print(f"Constraints with |dev| > 0.05:  {np.sum(np.abs(deviations) > 0.05)} / {n_constraints}")

print(f"\nTop-20 constraints by |deviation from prior|:")
idx = np.argsort(-np.abs(deviations))[:20]
for i in idx:
    c = model.phonology.constraints[i]
    print(f"  {repr(c):30s}  w={weights[i]:.4f}  μ={prior_mu[i]:.4f}  "
          f"Δ={deviations[i]:+.5f}")

overall_ok = (np.max(np.abs(deviations)) > 0.001)
print(f"\nWeight deviation check: {'OK — weights moved' if overall_ok else 'FAIL — weights frozen at priors'}")

# ---------------------------------------------------------------------------
# Suffix-focused report: what URs appear as suffixes?
# ---------------------------------------------------------------------------
print("\n" + "="*65)
print("SUFFIX ANALYSIS")
print("="*65)

parse_post = model._training_state.parse_posterior
suffix_triples = {(u, s): cnt for (cls, u, s), cnt in parse_post.items() if cls == 'suffix'}
if suffix_triples:
    total_suf = sum(suffix_triples.values())
    by_ur = {}
    for (ur, sr), cnt in suffix_triples.items():
        by_ur.setdefault(ur, {})
        by_ur[ur][sr] = by_ur[ur].get(sr, 0) + cnt
    for ur, sr_dict in sorted(by_ur.items(), key=lambda x: -sum(x[1].values()))[:15]:
        total = sum(sr_dict.values())
        sr_str = ', '.join(f'[{sr}]({cnt/total:.0%})' for sr, cnt in
                           sorted(sr_dict.items(), key=lambda x: -x[1]))
        print(f"  /{ur}/ → {sr_str}  (mass={total/total_suf:.3f})")
    # Key alternations
    print("\n--- Key alternation check ---")
    for ur in ['z', 'd']:
        if ur in by_ur:
            sr_dict = by_ur[ur]
            total = sum(sr_dict.values())
            srs = sorted(sr_dict.items(), key=lambda x: -x[1])
            print(f"  /{ur}/ surfaces as: " +
                  ', '.join(f'[{s}]×{cnt}({cnt/total:.1%})' for s, cnt in srs))
        else:
            print(f"  /{ur}/ never selected as suffix UR")
else:
    print("  (no suffix parse posterior — check model configuration)")

print()
model.print_sr_types()
print("\nTop-10 UR report (stem):")
model.print_ur_report()
